--Създаване на таблица Category
create table Category(
Id int IDENTITY primary key,
QuestionCategory int
);

--Създаване на таблица Questions
create table Questions(
Id int IDENTITY primary key,
Question varchar(2500),
A varchar(100),
B varchar(100),
C varchar(100),
D varchar(100),
Answear varchar(100),
CategoryId int,
constraint fk_qustion_category
foreign key (CategoryId)
references Category(Id),);

--Създаване на таблица Countries
create table Countries(
Id int primary key identity,  
Name varchar(50) unique 
);
go

--Създаване на таблица Towns
create table Towns(
Id int primary key identity,
Name varchar(50),
CountryId int not null,
constraint fk_towns_countries  
foreign key (CountryId)   
references Countries (Id) 
);

--Създаване на таблица Friend
create table Friend(
Id int IDENTITY primary key,
Name varchar(30),
Age int,
HomeTownId int,
constraint fk_friend_towns
foreign key (HomeTownId)
references Towns(Id));

--Създаване на таблица Player
create table Player(
Id int IDENTITY primary key,
Name varchar(30),
Age int,
HomeTownId int,
constraint fk_player_towns
foreign key (HomeTownId)
references Towns(Id),
FriendId int,
constraint fk_players_towns
foreign key (FriendId)
references Friend(Id));

